package Day806;
//导包，创建，使用

import java.util.Scanner;

public class Scanner1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        System.out.println("输入的数字是" + num);
        String str = sc.next();
        System.out.println("输入的字符是 "+ str);

    }


}
